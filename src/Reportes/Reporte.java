    public void Productos(String valor) {
        int[] numeros = new int[1];
        Conneccion mysql = new Conneccion();
        Connection cn = mysql.conectar();
        String aSQL = "Select cveProducto,cantidad From detVentas group by (cveProducto) order by cantidad ";
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(aSQL);
            while (rs.next()) {
                numeros[0] = rs.getInt("cantidad");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        Graficas bc = new Graficas(numeros, "Tlapaleria HC", "Productos");
        bc.setVisible(true);
        bc.setSize(440, 440);
        bc.setLocationRelativeTo(null);
        bc.setResizable(false);
    }

